--15
SELECT 
    SUPPLIER_ID,
    SUPPLIER_NAME,
    COALESCE(
        CAST(CONTACT AS VARCHAR),
        EMAIL,
        ADDRESS
    ) AS CONTACTDETAILS
FROM LMS_SUPPLIERS_DETAILS;

