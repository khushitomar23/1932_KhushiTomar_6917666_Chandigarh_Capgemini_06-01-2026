SELECT 
    MEMBER_ID,
    UPPER(LEFT(MEMBER_NAME,1)) + 
    LOWER(SUBSTRING(MEMBER_NAME,2,LEN(MEMBER_NAME))) AS Name
FROM LMS_MEMBERS
WHERE CITY IN ('CHENNAI','DELHI');

--12
SELECT DISTINCT
    BOOK_TITLE + '_is_written_by_' + AUTHOR AS BOOK_WRITTEN_BY
FROM LMS_BOOK_DETAILS;

--13
SELECT AVG(PRICE) AS AVERAGEPRICE
FROM LMS_BOOK_DETAILS
WHERE CATEGORY = 'JAVA';

--14
